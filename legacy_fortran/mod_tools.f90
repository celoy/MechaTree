subroutine random_set(ISEED)
    implicit none
    integer, intent(in) :: ISEED
    integer :: i_size
    integer, dimension(:), allocatable :: seed
    !
    call random_seed(SIZE = i_size) 
    allocate(seed(i_size))
    seed=ISEED
    call random_seed(PUT = seed(1:i_size))
    deallocate(seed)
    !
end subroutine random_set

