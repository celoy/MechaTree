SUBROUTINE cross_product(A, B, C)           ! Cross product of vectors

      IMPLICIT NONE                                ! no default typing

      real(kind=working_precis), DIMENSION(3), INTENT (IN)  :: A 
      real(kind=working_precis), DIMENSION(3), INTENT (IN)  :: B  
      real(kind=working_precis), DIMENSION(3), INTENT (OUT) :: C  


      C(1) = A(2)*B(3) - A(3)*B(2)                 ! compute cross product components
      C(2) = A(3)*B(1) - A(1)*B(3)
      C(3) = A(1)*B(2) - A(2)*B(1)

      RETURN

END SUBROUTINE CROSS_PRODUCT

subroutine rperm(N, p)
    implicit none
    integer, intent(in)                 :: N
    integer, dimension(:), intent(out)  :: p
!*****************************************************************************80  
! rperm gives a random permutation of a (1:N)
!-------------------------------------------------------------------------------  
    integer                     :: i, j, k
    integer                     :: temp
    real(kind=working_precis)   :: u

    p = (/ (i, i=1,N) /)

    do j = N,2,-1
        call random_number(u)
        k = floor(j*u) + 1
!       Exchange p(k) and p(j)
        temp = p(k)
        p(k) = p(j)
        p(j) = temp
    end do
end subroutine rperm


