subroutine rperm2(N, p)
    implicit none
    integer, intent(in)                 :: N
    integer, dimension(:), intent(out)  :: p
!*****************************************************************************80  
! RPERM2 gives a random permutation of a (1:N)
!-------------------------------------------------------------------------------  
    integer                     :: i, j, k
    integer                     :: temp
    real(kind=working_precis)   :: u

    p = (/ (i, i=1,N) /)

    do j = N,2,-1
        call random_number(u)
        k = floor(j*u) + 1
!       Exchange p(k) and p(j)
        temp = p(k)
        p(k) = p(j)
        p(j) = temp
    end do

end subroutine rperm2

!*****************************************************************************80  
FUNCTION random_normal() RESULT(fn_val)
implicit none
real(kind=working_precis) :: fn_val
real(kind=working_precis) :: r1, r2, pi=4.0d0 * atan ( 1.0d0 )

call random_number ( r1 )
call random_number ( r2 )
fn_val =  sqrt ( -2.0d0 * log ( r1 ) ) * cos ( 2.0d0 * pi * r2 )
RETURN
END FUNCTION random_normal

